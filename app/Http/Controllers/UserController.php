     * @return \Illuminate\Http\Response
     */
    public function index()
    {

        $this->data['user'] = Auth::user();
        if($this->data['user']->level == "master") {
            $this->data['users'] = User::where('level','<>','master')->get();
        } else if($this->data['user']->level == "admin") {
            $this->data['users'] = User::where('level','<>','master')->where('level','<>','admin')->get();
        }

        return view('pages.user.index',$this->data);
    }

